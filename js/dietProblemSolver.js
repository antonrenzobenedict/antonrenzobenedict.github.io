function simplex(){

    var matrixTable = addSlackVariables(); // get the values from the table
    var infeasible = false;

    var isDone = checkMatrix(matrixTable);
    while(!isDone){
        var pivotCol = pivotColumn(matrixTable);
        var pivotElem = pivotElement(matrixTable, pivotCol);
        var matrixTable = gaussJordan(matrixTable, pivotCol, pivotElem);
        isDone = checkMatrix(matrixTable);
        iteration++;
        if(iteration > 500000){
             $('#modal2').modal('open');
             infeasible = true;
             break;
        }
    }
    
    if(!infeasible) printMatrix(matrixTable);
}

function checkMatrix(matrixTable){

    var isDone = true;
    var lastRow = matrixTable.length - 1;

    // checks if the values in the last row is not a negative number
    for( var i = 0; i<matrixTable[lastRow].length; i++ ){
        if(matrixTable[lastRow][i] < 0){
          // returns FALSE if there is a negative number found which means the process is not yet done
          isDone = false;
        }
    }

    return isDone;
}

function pivotColumn(matrixTable){
    
    var row = matrixTable.length - 1;
    var col = matrixTable[row].length;
    var pivotCol = 0;
    var max = 0;

    // traverses the last row to check for the negative number with the highest magnitude
    for(var i = 0; i<col; i++ ){
        if( matrixTable[row][i] < 0 ){
            if( Math.abs(matrixTable[row][i]) > max ){
                max = Math.abs(matrixTable[row][i]);
                pivotCol = i;
            } 
        }
    }
    
    // returns the column number for the pivot column
    return pivotCol;
}

function pivotElement(matrixTable, pivotCol){

    var row = matrixTable.length;
    var col = matrixTable[row-1].length - 1;
    var pivotElem = 0;
    var min = 0;
    var testRatio = []; // array for storing the TEST RATIO

    // computes for the test ratio by dividing the solution col and the pivot col
    for(var i = 0; i<row; i++){
        testRatio[i] = ( matrixTable[i][col] / matrixTable[i][pivotCol] );
    }

    // finds the lowest value in the test ratio (0 and negative number is not included)
    for(var i = 0; i<testRatio.length; i++){
        if(testRatio[i] > 0){
            if(min == 0){ 
                min = testRatio[i]; 
                pivotElem = i;
            }
            else if(testRatio[i] < min){
                min = testRatio[i];
                pivotElem = i;
            }
        }  
    }

    // returns the row number for the pivot element
    return pivotElem;
}

function gaussJordan(matrixTable, pivotCol, pivotElem){

    var row = matrixTable.length;
    var col = matrixTable[row-1].length;
    var pivotValue = matrixTable[pivotElem][pivotCol];

    // performs the Gauss-Jordan elimination to normalize the pivot column by using the pivot element row
    for(var i = 0; i<matrixTable[pivotElem].length; i++){
        matrixTable[pivotElem][i] = matrixTable[pivotElem][i] / pivotValue;
    }

    for(var i = 0; i<row; i++){
        if(pivotElem != i){
            var multiplier = matrixTable[i][pivotCol];
            for(var j = 0; j<col; j++){
                matrixTable[i][j] = matrixTable[i][j] - (matrixTable[pivotElem][j] * multiplier);
            }
        }
    }

    return matrixTable;
}

